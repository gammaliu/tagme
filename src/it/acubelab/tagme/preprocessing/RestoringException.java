package it.acubelab.tagme.preprocessing;

public class RestoringException extends RuntimeException
{
	private static final long serialVersionUID = 1764773389051231719L;
	public RestoringException() {
		super();
	}
	public RestoringException(String message, Throwable cause) {
		super(message, cause);
	}
	public RestoringException(String message) {
		super(message);
	}
	public RestoringException(Throwable cause) {
		super(cause);
	}
}